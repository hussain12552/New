print("Hello, World!")
def greet(name):
    """Function to greet a person with their name."""
    return f"Hello, {name}!"
input_name = input("Enter your name: ")
print(greet(input_name))
# This is a simple Python script that greets the user.
# It defines a function `greet` that takes a name as an argument and returns a greeting message.
print("This script is a demonstration of a simple Python function.")
# The script also includes a user input prompt to get the name from the user.

def farewell(name):
    """Function to bid farewell to a person with their name."""
    return f"Goodbye, {name}!"
print(farewell(input_name))

# The farewell function is defined to provide a goodbye message.
# This script can be used to demonstrate basic Python function definitions and user input handling.